public class Operacoes {
    float result;
    public float SumN (float N1, float N2) {
        result = N1+N2;
        return result;
    }
    
    public float SubN (float N1, float N2) {
        result = N1-N2;
        return result;
    }
    
    public float MulN (float N1, float N2) {
        result = N1*N2;
        return result;
    }
    
    public float DivN (float N1, float N2) {
       
        result = N1/N2;
        return result;
    }
}
